// Set default language on page load
// Always default to Albanian (sq) on first load
// If a language is already set in localStorage, use it
// Otherwise, set to 'sq'
document.addEventListener('DOMContentLoaded', () => {
  let lang = localStorage.getItem('lang');
  if (!lang) {
    lang = 'sq'; // Default to Albanian
    localStorage.setItem('lang', 'sq');
  }
  setLanguage(lang);
}); 

function setLanguage(lang) {
  localStorage.setItem('lang', lang);

  // Update dropdown button text and active state
  const langNames = { sq: 'Shqip', mk: 'Македонски', en: 'English' };
  const langDropdown = document.getElementById('langDropdown');
  const langDropdownMobile = document.getElementById('langDropdownMobile');
  
  if (langDropdown) {
    const span = langDropdown.querySelector('span');
    if (span) span.textContent = langNames[lang];
  }
  if (langDropdownMobile) {
    const img = langDropdownMobile.querySelector('img');
    if (img) img.src = `images/flags/${lang}.png`;
  }

  // Update active state in dropdown items
  document.querySelectorAll('.dropdown-menu a.dropdown-item').forEach(item => {
    item.classList.remove('active');
    const itemText = item.textContent.trim();
    if (itemText === langNames[lang]) {
      item.classList.add('active');
    }
  });

  // Instantly update all translatable text
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  // Show the body after translations are applied
  document.body.style.visibility = 'visible';
} 